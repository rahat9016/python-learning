# print char from string using for loop
for item in 'python':
    print(item) 
    """
        #output
        p
        y
        t
        h
        o
        n
    """
# print number from list using for loop
for item in [1, 2, 3, 4, 5]:
    print(item)
    """
        #output
        1
        2
        3
        4
        5
    """
# print name from list using for loop
for item in ["Rahat", "&", "Tanzina", "Love's", "Together"]:
    print(item)
    """
        #output
        Rahat
        &
        Tanzina
        Love's
        Together
    """
